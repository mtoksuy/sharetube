			<div class="matome_content_block">
				<div class="matome_content_block_text">
					<pre><h3 class="h3_heading_1">まとめインセンティブ制度と特徴</h3>まとめ作成者全員が対象となり、Sharetubeアカウントの取得した方が「まとめ」を作成し、毎月の総PVを元にインセンティブが発生致します。
なお、「まとめ」がある限り永久インセンティブが発生致します。
		<h3 class="h3_heading_1">インセンティブの計算方法</h3>純粋にPV計算のみと致します。</pre>

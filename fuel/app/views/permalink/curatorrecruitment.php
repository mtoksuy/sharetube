					<h2 class="h2_heading_1">まとめインセンティブについて</h2>
				</div>
			</div>
			<div class="matome_content_block">
				<div class="matome_content_block_text">
					<pre><h3 class="h3_heading_1">まとめインセンティブプログラムとは</h3>「まとめインセンティブプログラム」は、「Sharetube」がユーザーにとってより良いサービスとなるべく、皆さんの健全なまとめ作成を支援することを目的とした支援金プログラムの総称です。
		
インターネットで収益を方法としまして、サイトを開いてその中で、広告、アフィリエイトなどで収益を上げる方法がございます。ですが、サイトを開く事一つとっても大きな壁がある事は周知の通りです。そこで誰でも気軽に自分が好きな情報を「まとめ」られる場所を作りました。</pre>
				</div>
			</div>
			<div class="matome_content_block">
				<div class="matome_content_block_text">
					<pre><h3 class="h3_heading_1">何も考えずに始められる…『インセンティブ制度』</h3>弊社がその時点で良質と考える「活動」や「まとめ」を支援・奨励することを目的とした、まとめ作成者全員が対象となる基本制度です。複雑な申請や審査は一切不要。作っていただいたまとめの内容やアクセス数を集計し、インセンティブを決定するので、自分が作成したまとめの広告がクリックされない場合でも、インセンティブが分配されます。
		
※「まとめ」を作成をするには、Sharetubeアカウント（登録無料）の取得が必要です
※まだSharetubeアカウントをお持ちでない方は、<a href="http://sharetube.jp/signup/" target="_blank">こちら</a>から取得してください</pre>
				</div>
			</div>

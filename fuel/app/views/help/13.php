			<pre>Sharetubeでは違法行為を厳しく処罰しています。
罰せられるのは運営側ではなく書き手である事を理解して下さい。


・文章を正しく引用する方法
引用する場合は引用元の記事の3分の1以下にする事。
基本は3行以下を引用するのが好ましいです。

まとめの構成内容でもオリジナル文章と引用の割合は重要です。
7:3が好ましいとSharetubeは考えています。
オリジナル文章が主で引用が従であるべきです。


・画像を正しく引用する方法
有料画像の場合は必ず契約をしてから使用する事。
フリー素材であっても引用はする事。
引用は必ず画像があるページでのURLで引用記載をする事。


細かいルールは利用規約に書かれていますのでまとめを作成する場合は一読お願いいたします。</pre>

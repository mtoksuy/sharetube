<h2 class="heading_3">ダイヤスカウトのお値段</h2>

<p>重要なのはお値段です</p>

<p>ここが高いか安いかで何回もするであろうスカウト料金が変わってきますが・・・</p>


<p>残念！</p>


<p>1回のダイヤスカウト料金は<b>500</b>円なのです。</p>


<p>ちょい高w</p>


<p class="m_0">レア度：★★★★★ が確率：1.25% となっていますので単純計算だと</p>
<p>11連スカウト×7回 + 1スカウト×3回の合計80回スカウトを行い</p>

<p>ようやく レア度：★★★★★ が一体手に入るといった低確率</p>

<p>ちなみにお値段は</p>

<p class="m_0"><b style="font-size: 150%;">36,500</b>円ぶっこむ計算になります。</p>
